
public class Calcular {
	
	
	public static float soma(float val1, float val2) {
		return val1 + val2;
	}
	
	public static float subtrai(float val1, float val2) {
		return val1 - val2;
	}
	
	public static float multiplica(float val1, float val2) {
		return val1 * val2;
	}
	
	public static float divide(float val1, float val2) {
		return val1 / val2;
	}
	
	

}
